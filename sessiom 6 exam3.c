#include<stdio.h>

void main(){
	printf("The number 555.55 in various forms : \n");
	printf ("In float form without modifiers: \n");
	printf("[%f]\n", 555.55);
	print("IN exponential form without any modifiers: \n");
	printf("[%e]\n", 555.55);
	printf("in float form with - modifiers: \n");
	printf("[%-f]\n",555.55);
	printf("In float form with fegit string 10.3 as modifiers\n");
	
	printf("[%10.3f]\n",555.55);

}
